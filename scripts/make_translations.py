for lang_code, translations in languages.items():
    lang_dir = os.path.join(locale_dir, lang_code, 'LC_MESSAGES')
    os.makedirs(lang_dir, exist_ok=True)
    
    po = polib.POFile()
    po.metadata = {
        'Project-Id-Version': '1.0',
        'Report-Msgid-Bugs-To': '',
        'POT-Creation-Date': '2024-04-25 10:00+0000',
        'PO-Revision-Date': '2024-04-25 10:00+0000',
        'Last-Translator': 'Auto',
        'Language-Team': lang_code,
        'MIME-Version': '1.0',
        'Content-Type': 'text/plain; charset=utf-8',
        'Content-Transfer-Encoding': '8bit',
        'Language': lang_code,
    }
    
    for msgid, msgstr in translations.items():
        entry = polib.POEntry(
            msgid=msgid,
            msgstr=msgstr
        )
        po.append(entry)
        
    po_path = os.path.join(lang_dir, 'django.po')
    mo_path = os.path.join(lang_dir, 'django.mo')
    po.save(po_path)
    po.save_as_mofile(mo_path)
    print(f"Generated {lang_code} translations.")
